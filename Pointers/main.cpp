#include <iostream>

using namespace std;

int main() {
    cout << "Pointers are the types of objects with which we indirectly access other objects" << endl;

    int *p; //This is type pointer to an integer

    cout << "assigning a value directly to a pointer is not allowed" << endl;

    int x;

    p = &x; // This is interpreted as an address of x.




}
